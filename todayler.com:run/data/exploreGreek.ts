function greekInstruction(title: string): string {
  return `Δοκίμασε τη δραστηριότητα «${title}» αργά και ήρεμα για 1–3 λεπτά, με μικρές παύσεις ώστε να δεις πώς ανταποκρίνεται το μωρό σου.`;
}

function greekWhy(category: ExploreActivity["category"]): string {
  if (category === "spark") {
    return "Ενισχύει την προσοχή, την παρατήρηση και τις πρώτες νοητικές συνδέσεις μέσα από απλή, επαναλαμβανόμενη αλληλεπίδραση.";
  }
  if (category === "move") {
    return "Υποστηρίζει τον έλεγχο σώματος, την ισορροπία και τον συντονισμό με ασφαλή, σταδιακή κίνηση.";
  }
  return "Δυναμώνει τον δεσμό, την κοινωνική αλληλεπίδραση και την αίσθηση ασφάλειας μέσα από κοινό παιχνίδι.";
}

export function localizeExploreActivity(activity: ExploreActivity, language: AppLanguage): ExploreLocalizedContent {
  if (language !== "el") {
    return {
      title: activity.title,
      instruction: activity.instruction,
      why: activity.why,
    };
  }

  const title = EXPLORE_TITLE_EL[activity.id] ?? "Δραστηριότητα εξερεύνησης";
  return {
    title,
    instruction: greekInstruction(title),
    why: greekWhy(activity.category),
  };
}
