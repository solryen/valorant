const dictByLang: Record<OnboardingPaywallLanguage, Dict> = { en, el };

export function tOnbPw(
  lang: OnboardingPaywallLanguage,
  key: string,
  vars?: Record<string, string | number>,
  gender?: BabyGender
): string {
  const base = dictByLang[lang]?.[key] ?? en[key] ?? key;
  const interpolated = !vars
    ? base
    : Object.keys(vars).reduce((acc, k) => acc.replaceAll(`{${k}}`, String(vars[k])), base);
  if (lang !== "el") return interpolated;
  return resolveGreekGenderAlternatives(interpolated, gender);
}
